import { Job } from "../models/job.model.js";
import { User } from "../models/user.model.js";
// admin post krega job
export const postJob = async (req, res) => {
    try {
        const { title, description, requirements, salary, location, jobType, experience, position, companyId } = req.body;
        const userId = req.id;

        if (!title || !description || !requirements || !salary || !location || !jobType || !experience || !position || !companyId) {
            return res.status(400).json({
                message: "Somethin is missing.",
                success: false
            })
        };
        const job = await Job.create({
            title,
            description,
            requirements: requirements.split(","),
            salary: Number(salary),
            location,
            jobType,
            experienceLevel: experience,
            position,
            company: companyId,
            created_by: userId
        });
        return res.status(201).json({
            message: "New job created successfully.",
            job,
            success: true
        });
    } catch (error) {
        console.log(error);
    }
}
// student k liye
export const getAllJobs = async (req, res) => {
    try {
        const keyword = req.query.keyword || "";
        const userId = req.id; // Assuming user is authenticated
        
        const user = await User.findById(userId);
        const knownSkills = user?.profile?.skills || [];

        const query = {
            $or: [
                { title: { $regex: keyword, $options: "i" } },
                { description: { $regex: keyword, $options: "i" } },
            ]
        };
        
        let jobs = await Job.find(query).populate("company");
        
        jobs = jobs.map(job => {
            const matchedSkills = job.requirements.filter(skill => knownSkills.includes(skill)).length;
            return { ...job.toObject(), matchedSkills };
        });
        
        jobs.sort((a, b) => b.matchedSkills - a.matchedSkills);
        
        return res.status(200).json({
            jobs,
            success: true
        });
    } catch (error) {
        console.log(error);
        res.status(500).json({ message: "Server error", success: false });
    }
};

